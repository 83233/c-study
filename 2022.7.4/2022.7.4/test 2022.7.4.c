//包含一个叫stdio.h的文件
//std-标准 standard input output
#include<stdio.h>
//int是整型的意思
//main前面的int表示main函数调用返回一个整型值（即整数）
int main()//主函数-程序的入口（main函数有且只能有一个）
{
	//这里完成任务
	//函数-printf--print function
	char ch = 'A';//内存（可理解为用ch代替A字符）;
	printf("hello，world--%c\n",ch);//打印字符格式的数据
	//在屏幕上输出hello world
	//\n用于换行
	//库函数-c语言本身提供使用的函数;;
		return 0;//返回 0

}